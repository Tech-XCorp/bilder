######################################################################
#
# Version
#
######################################################################

ADOLC_BLDRVERSION=${ADOLC_BLDRVERSION:-"2.0.0"}

######################################################################
#
# Other values
#
######################################################################

if test -z "$ADOLC_BUILDS"; then
  ADOLC_BUILDS=ser
fi

ADOLC_DEPS=autotools
ADOLC_UMASK=002

######################################################################
#
# Add to paths
#
######################################################################

######################################################################
#
# Launch adolc builds.
#
######################################################################

buildAdolc() {
  if bilderUnpack adolc; then
    if bilderConfig adolc ser; then
      bilderBuild adolc ser
    fi
  fi
}

######################################################################
#
# Test adolc
#
######################################################################

testAdolc() {
  techo "Not testing adolc."
}

######################################################################
#
# Install adolc
#
######################################################################

installAdolc() {
  bilderInstall adolc ser
}
