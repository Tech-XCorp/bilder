######################################################################
#
# Launch ftgl builds.
#
######################################################################

buildFtgl() {
  if bilderUnpack ftgl; then
    if bilderConfig ftgl ser; then
      bilderBuild -m make ftgl ser "" "ECHO=echo"
    fi
  fi
}

