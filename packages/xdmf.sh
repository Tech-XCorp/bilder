######################################################################
#
# Version
#
######################################################################
XDMF_BLDRVERSION=${XDMF_BLDRVERSION:-"unknown"}

######################################################################
#
# Other values
#
######################################################################
if test -z "$XDMF_BUILDS"; then
  XDMF_BUILDS=ser
fi

XDMF_DEPS=autotools
XDMF_UMASK=002

######################################################################
#
# Add to paths
#
######################################################################

######################################################################
#
# Launch xdmf builds.
#
######################################################################

buildXdmf() {
  if bilderUnpack xdmf; then
    if bilderConfig -c xdmf ser; then
      bilderBuild xdmf ser
    fi
  fi
}

######################################################################
#
# Test xdmf
#
######################################################################

testXdmf() {
  techo "Not testing xdmf."
}

######################################################################
#
# Install xdmf
#
######################################################################

installXdmf() {
  bilderInstall xdmf ser
}
