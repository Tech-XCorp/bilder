######################################################################
#
# Version
#
######################################################################

GSL_BLDRVERSION=${GSL_BLDRVERSION:-"1.15"}

######################################################################
#
# Other values
#
######################################################################

GSL_BUILDS=${GSL_BUILDS:-"ser"}
GSL_DEPS=

######################################################################
#
# Add to path
#
######################################################################

addtopathvar PATH $CONTRIB_DIR/gsl/bin

######################################################################
#
# Launch gsl builds.
#
######################################################################

buildGsl() {
  if bilderUnpack gsl; then
    if bilderConfig gsl ser; then
      bilderBuild gsl ser
    fi
  fi
}

######################################################################
#
# Test gsl
#
######################################################################

testGsl() {
  techo "Not testing gsl."
}

######################################################################
#
# Install gsl
#
######################################################################

installGsl() {
  bilderInstall -r gsl ser gsl
}

