ROOT_BLDRVERSION=${ROOT_BLDRVERSION:-"5.34.14"}

######################################################################
#
# Other values
#
######################################################################

ROOT_BUILDS=${ROOT_BUILDS:-"ser"}
ROOT_DEPS=cmake

######################################################################
#
# Launch root builds.
#
######################################################################

buildGeant4() {
  if bilderUnpack root; then
    if bilderConfig -c root ser "$CMAKE_SUPRA_SP_ARG"; then
      bilderBuild root ser ""
    fi
  fi
}

######################################################################
#
# Test root
#
######################################################################

testGeant4() {
  techo "Not testing root."
}

######################################################################
#
# Install root
#
######################################################################

installGeant4() {
  bilderInstall -r root ser root
}

